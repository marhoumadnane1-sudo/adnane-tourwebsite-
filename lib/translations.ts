export type Lang = keyof typeof translations
export type TranslationKeys = typeof translations.en

export function t(lang: Lang, section: keyof TranslationKeys, key: string): string {
  const val = (translations[lang] as any)?.[section]?.[key]
  if (val !== undefined && !Array.isArray(val)) return String(val)
  const fallback = (translations.en as any)?.[section]?.[key]
  if (fallback !== undefined && !Array.isArray(fallback)) return String(fallback)
  return key
}

export function tArr(lang: Lang, section: keyof TranslationKeys, key: string): string[] {
  const val = (translations[lang] as any)?.[section]?.[key]
  if (Array.isArray(val)) return val
  const fallback = (translations.en as any)?.[section]?.[key]
  return Array.isArray(fallback) ? fallback : []
}
